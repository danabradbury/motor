    if (steps == 0) return;

    // Set direction based on step sign
    if (steps > 0) {
        HAL_GPIO_WritePin(DIR1_GPIO_Port, DIR1_Pin, GPIO_PIN_RESET);  // Forward
    } else {
        HAL_GPIO_WritePin(DIR1_GPIO_Port, DIR1_Pin, GPIO_PIN_SET); // Reverse
        steps = -steps;  // Make positive for pulse counting
    }

   /* // At 100kHz PWM: 0.01ms per step
    uint32_t delayTime = steps / 100;  // Convert to milliseconds
    if (delayTime == 0) delayTime = 1;  // Minimum 1ms for very small moves*/

    // Enable DWT cycle counter if not already enabled
